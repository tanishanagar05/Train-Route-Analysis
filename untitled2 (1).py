import numpy as np

def parse_time(t):
    try:
        h, m, s = map(int, t.strip().split(':'))
        total = h * 60 + m + s / 60
        return np.nan if total == 0 else total
    except:
        return np.nan

df['Arrival_min']   = df['Arrival_time'].apply(parse_time)
df['Departure_min'] = df['Departure_Time'].apply(parse_time)
