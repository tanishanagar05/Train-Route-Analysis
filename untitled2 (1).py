def compute_duration(group):
    dep_rows = group[group['Departure_min'].notna()]
    arr_rows = group[group['Arrival_min'].notna()]
    if dep_rows.empty or arr_rows.empty:
        return np.nan
    dep = dep_rows['Departure_min'].iloc[0]
    arr = arr_rows['Arrival_min'].iloc[-1]
    duration = arr - dep
    if duration < 0:
        duration += 24 * 60  # Overnight train correction
    return duration

train_duration = df.sort_values(['Train_No','SN']).groupby('Train_No').apply(compute_duration)
train_duration = train_duration.rename('Journey_Duration_min').reset_index()
